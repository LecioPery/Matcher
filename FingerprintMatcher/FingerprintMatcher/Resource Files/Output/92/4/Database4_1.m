close all;
clear;
clc;
x = 0:2000:60000;
xq = 0:0.5:60000;

y0 = [0 0.00526316 0.00526316 0.00526316 0.00526316 0.00526316 0.00526316 0.00526316 0.00526316 0.00526316 0.00526316 0.00526316 0.00526316 0.0315789 0.152632 0.273684 0.410526 0.531579 0.626316 0.7 0.768421 0.821053 0.857895 0.884211 0.926316 0.952632 0.963158 0.989474 0.994737 0.994737 1];
